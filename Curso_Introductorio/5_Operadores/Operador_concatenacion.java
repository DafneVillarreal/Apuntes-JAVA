package Basico;

public class Operador_concatenacion {
    public static void main (String[] args) {
        String Nombre = "El Tunas", Profesion = "Cerrajero";
        
        System.out.print ("Mi nombre es " +Nombre +"y mi profesion es ser " +Profesion);
    }
    
    /*
    El operador de concatenacion ( + ) nos permite concatenar cadenas de texto
    y variables en la impresion de datos en pantalla 
    */
}
